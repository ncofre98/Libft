#include "libft.h"

int				ft_lstsize(t_list *lst)
{
	unsigned int	i;
	t_list			*tmp;

	i = 0;
	tmp = lst;
	while (tmp != NULL)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
